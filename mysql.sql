-- Creating USERS table
CREATE TABLE USERS (
   USER_ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
   USERNAME VARCHAR(50),
   FIRST_NAME VARCHAR(50),
   RESTAURANT_NAME VARCHAR(50),
   LAST_NAME VARCHAR(50),
   EMAIL VARCHAR(50),
   PASSWORD INTEGER
);

-- Creating RESTAURANTS table--
CREATE TABLE RESTAURANTS (
   RESTAURANT_ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
   USERNAME VARCHAR(50),
   RESTAURANT_NAME VARCHAR(50),
   EMAIL VARCHAR(50),
   RESTAURANT_OWNER VARCHAR(50),
   ADDRESS varchar(255),
   OPENING_HOURS VARCHAR(255),
   PRICE VARCHAR(10),
   COOKINGTYPE VARCHAR(50),
   CONTACT VARCHAR(50),
   MENU varchar (255),
   SPECIALDIET VARCHAR(255),
   RATING VARCHAR(255),
   PASSWORD INTEGER
);

-- Creating USER_RATES_RESTAURANT table--
CREATE TABLE USER_RATES_RESTAURANT (
   USER_ID INT,
   RESTAURANT_ID INT,
   FOREIGN KEY (USER_ID) REFERENCES USERS (USER_ID),
   FOREIGN KEY (RESTAURANT_ID) REFERENCES RESTAUTANTS (RESTAURANT_ID),
   CONSTRAINT PK_RATING PRIMARY KEY (USER_ID, RESTAURANT_ID)
);

